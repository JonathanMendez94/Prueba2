    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            String codigo = jTable1.getValueAt(row, 1).toString();
            int resp = JOptionPane.showConfirmDialog(null, "En realidad desea eliminar", "Mensaje de Confirmacion", JOptionPane.YES_NO_OPTION);
            if (resp == 0) {
                int respEli = objBLGestionarMesa.confElimina(codigo);
                if (respEli == 0) {
                    JOptionPane.showMessageDialog(rootPane, "!! Error al momento de eliminar");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Exito!!! Registro borrado");
                    llenarMesa();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione para poder eliminar");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    void llenarMesa() {
        DefaultTableModel model;
        //arreglo con los titulos de la tabla
        String[] titulos = {"Id", "Codigo", "Num. Mesa", "Num. Comensales"};
        model = new DefaultTableModel(null, titulos);//enviamos el arreglo en el modelo de la tabla

        String[] fila = new String[4];

        //traemos valores a presentar en la tabla
        String[][] valores = objBLGestionarMesa.DatosMesa();

        //agregamos los valores en el arreglo
        for (int i = 0; i < valores.length; i++) {
            fila[0] = (i + 1) + "";
            for (int j = 0; j < 3; j++) {
                fila[j + 1] = valores[i][j];
            }
            model.addRow(fila);
        }
        jTable1.setModel(model); //agregamos valores en la tabla
    }

