    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String numMesa = jTextField1.getText();
        String resMes = objBLGestionarMesa.obtCodMesa(numMesa); //validamos existencia de numero de mesa
        if (resMes == null) {
            String numComen = jTextField2.getText();
            String codigo = objBLGeneradorCodigos.envioCod("cm", "mesa");
            //variables para el registro
            int mesa = Integer.parseInt(numMesa);
            int comen = Integer.parseInt(numComen);
            Mesa objMesa = new Mesa(codigo, mesa, comen);
            int resMesa = objBLGestionarMesa.envioMesa(objMesa);
            if (resMesa == 0) {
                JOptionPane.showMessageDialog(rootPane, "Mesa no ingresada");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Mesa ingresada");
                limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Numero de mesa existente");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

