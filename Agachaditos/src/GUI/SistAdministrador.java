    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String cedula = jTextField1.getText();
        String nombre = jTextField2.getText();
        String apellido = jTextField3.getText();
        String clave = jTextField4.getText();

        if (("".equals(cedula)) || ("".equals(nombre)) || ("".equals(apellido)) || ("".equals(clave))) {
            JOptionPane.showMessageDialog(rootPane, "Complete todo los campos de del registro");
        } else {
            String tipo = jtipo.getText();
            switch (tipo) {
                case "cu2":
                    String resChef = objBLGestionarChef.comprobarExistenciaChef(cedula);
                    if ("no".equals(resChef)) {
                        Chef objChef = new Chef(cedula, nombre, apellido, clave, tipo);
                        int resChe = objBLGestionarChef.envioChef(objChef);
                        if (resChe == 0) {
                            JOptionPane.showMessageDialog(rootPane, "Chef no registrado");
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Chef registrado");
                            jPanel1.setVisible(false);
                        }
                    }
                    if ("si".equals(resChef)) {
                        JOptionPane.showMessageDialog(rootPane, "Numero de cedula ya existente");
                        jTextField1.setText("");
                    }
                    break;
                case "cu3":
                    String resMese = objBLGestionarMesero.comprobarExistenciaMesero(cedula);
                    if ("no".equals(resMese)) {
                        Mesero objMesero = new Mesero(cedula, nombre, apellido, clave, tipo);
                        int resMes = objBLGestionarMesero.envioMesero(objMesero);
                        if (resMes == 0) {
                            JOptionPane.showMessageDialog(rootPane, "Mesero no registrado");
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Mesero registrado");
                            jPanel1.setVisible(false);
                        }
                    }
                    if ("si".equals(resMese)) {
                        JOptionPane.showMessageDialog(rootPane, "Numero de cedula ya existente");
                    }
                    break;
                case "client":
                    Cliente objCliente = objBLGestionarCliente.comprobarExistenciaCliente(cedula);
                    System.out.println(objCliente.getIdentificacion());
                    if (objCliente.getIdentificacion() == null) {
                        objCliente = new Cliente(cedula, nombre, apellido);
                        int resClien = objBLGestionarCliente.envioCliente(objCliente);
                        if (resClien == 0) {
                            JOptionPane.showMessageDialog(rootPane, "Cliente no registrado");
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Cliente registrado");
                            jPanel1.setVisible(false);
                        }
                    }
                    break;
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

