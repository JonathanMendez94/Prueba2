    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            String cedula = jTable1.getValueAt(row, 1).toString();
            String tipo = jtipo.getText();
            int resp = JOptionPane.showConfirmDialog(null, "En realidad desea eliminar el " + tipo, "Mensaje de Confirmacion", JOptionPane.YES_NO_OPTION);
            if (resp == 0) {
                int respEli = objBLGestionAdministrador.confElimina(tipo, cedula);
                if (respEli == 0) {
                    JOptionPane.showMessageDialog(rootPane, "!! Error al momento de eliminar");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Exito!!! Registro borrado");
                    llenarTabla();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione para poder eliminar");
        }
    }//GEN-LAST:event_jButton9ActionPerformed

