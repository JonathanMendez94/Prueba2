    private void btn3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn3ActionPerformed
        int[] resValida = new int[5]; //arreglo para almacenar resultados de validacion

        //recgemos valores y validamos a que se hayan complentado los datos       
        this.Mesero = (String) bx1.getSelectedItem();
        resValida[0] = objBLValidar.validarSeleccion(Mesero, "Mesero");

        this.Mesa = (String) bx2.getSelectedItem();
        resValida[1] = objBLValidar.validarSeleccion(Mesa, "Mesa");

        String SelePentrada = (String) box3.getSelectedItem();
        resValida[2] = objBLValidar.validarSeleccion(SelePentrada, "Plato de entrada");

        String SelePfuerte = (String) box4.getSelectedItem();
        resValida[3] = objBLValidar.validarSeleccion(SelePfuerte, "Plato fuerte");

        String SeleBebida = (String) box5.getSelectedItem();
        resValida[4] = objBLValidar.validarSeleccion(SeleBebida, "Bebida");

        //se valida el permiso de registro
        int permiso = objBLValidar.validaPermiso(resValida);

        if (permiso == 1) {//si el permiso es 1 se procede al realizar el registro
            //recogemos valores de las cantidades
            String canEntrada = txt4.getText();
            String canFuerte = txt5.getText();
            String cantBebida = txt6.getText();

            if (("0".equals(canEntrada)) || ("0".equals(canFuerte)) || "0".equals(cantBebida)) {//se valida que las cantidades no sean 0
                JOptionPane.showMessageDialog(rootPane, "Ingrese las cantidades del menu ");
            } else {
                //recogemos la cedula del cliente
                this.Cliente = txt1.getText();
                this.CodOrde = codOrden.getText();

                //transformamos a entero las cantidades
                int cEntrada = Integer.parseInt(canEntrada);
                int cFuerte = Integer.parseInt(canFuerte);
                int cBebida = Integer.parseInt(cantBebida);

                //enviamos valores a la clase orden para almacenarlos
                Orden objOrden = new Orden(CodOrde, SelePentrada, SelePfuerte, SeleBebida, cEntrada, cFuerte, cBebida);

                //enviamos a registrar
                int resOrden = objBLGestionarOrden.registrarOrden(objOrden);

                if (resOrden == 1) {
                    JOptionPane.showMessageDialog(rootPane, "Orden Agregada");
                    CargarEntrada();
                    Cargarfuerte();
                    Cargarbebida();
                    jPanel2.setVisible(true);
                    llenarTabla_2();
                    jButton1.setVisible(true);
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Algunos campos estan incompletos");
        }
    }//GEN-LAST:event_btn3ActionPerformed

