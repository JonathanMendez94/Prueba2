    private void btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2ActionPerformed
        generadorCodigo();//generamos codigo de orden

        //recogemos valores del cliente
        String identidicacion = (String) txt1.getText();
        String nombre = (String) txt2.getText();
        String apellido = (String) txt3.getText();
        //enviamos al objeto cliente
        Cliente objCliente = new Cliente(identidicacion, nombre, apellido);

        int resCliente = objBLGestionarCliente.envioCliente(objCliente);

        if (resCliente == 0) {
            JOptionPane.showMessageDialog(rootPane, "cliente no registrado");
        } else {
            JOptionPane.showMessageDialog(rootPane, "cliente registrado");
            jPanel1.setVisible(true);
            btn2.setVisible(false);
        }
    }//GEN-LAST:event_btn2ActionPerformed

    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        eliminaOrden(CodOrde);
        generadorCodigo();//generamos codigo de orden

        String cedula = txt1.getText();
        if (!"".equals(cedula)) {
            Cliente datosCliente = objBLGestionarCliente.comprobarExistenciaCliente(cedula);
            if (datosCliente.getIdentificacion() == null) {
                JOptionPane.showMessageDialog(rootPane, "registre usuario");
                btn2.setVisible(true);
            } else {
                txt2.setText(datosCliente.getNombre());
                txt3.setText(datosCliente.getApellido());
                jPanel1.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "ingrese cedula");
        }
    }//GEN-LAST:event_btn1ActionPerformed

