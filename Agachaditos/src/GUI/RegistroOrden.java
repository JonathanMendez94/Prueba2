    void eliminaOrden(String codO) {
        if (!"".equals(codO)) {
            //traemos confirmacion de la orden cancelada
            int resp = JOptionPane.showConfirmDialog(null, "En realidad desea eliminar la orden", "Mensaje de Confirmacion", JOptionPane.YES_NO_OPTION);
            if (resp == 0) {
                int resCanOrde = objBLGestionarOrden.eliminarOrden(codO);
                //validamos si se a eliminado la orden
                if (resCanOrde == 0) {
                    JOptionPane.showMessageDialog(rootPane, "La orden no a sido eliminada");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "La orden eliminada");
                    jButton1.setVisible(false);
                    jPanel2.setVisible(false);
                }
            }
        }
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Factura objFactura = new Factura(Cliente, Mesero, Mesa, CodOrde);
        if (!"".equals(objFactura.getCliente())) {
            int resFact = objBLGestionarFactura.procesarFactura(objFactura);
            if (resFact == 0) {
                JOptionPane.showMessageDialog(rootPane, "Factura no registrada");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Factura registrada");
                JOptionPane.showMessageDialog(rootPane, "Se generara otra factura");
                this.CodOrde = "";//vaciamos codigo de orden
                oculta();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ah!!! ocurido un error intente registrar otra vez");
            eliminaOrden(CodOrde);
            oculta();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

